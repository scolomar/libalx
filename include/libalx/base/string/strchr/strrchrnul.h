/******************************************************************************
 ******* inline ***************************************************************
 ******************************************************************************/
inline
ptrdiff_t alx_strrchrnul	(const char str[restrict], char c)
{
	const char	*p = strrchr(str, c);

	if (!p)
		return	strlen(str);
	return	p - str;
}


