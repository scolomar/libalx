/*
 * [[gnu::format(printf, 1, 2)]]
 * void	alx_perrorx(const char *restrict fmt, ...);
 */
#define alx_perrorx(fmt, ...)		do				\
{									\
	alx__perrorx__(__FILE__, __LINE__, __func__, errno,		\
				(fmt)  __VA_OPT__(,)  __VA_ARGS__);	\
} while (0)


