/******************************************************************************
 ******* define ***************************************************************
 ******************************************************************************/
#define BIT_SET(x, bit)		__extension__(				\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	b_	= (bit);				\
									\
	alx_Static_assert_unsigned(*x_);				\
	alx_assert_bit_fits_type(b_, *x_);				\
									\
	*x_	|= 1ull << b_;						\
	*x_;								\
}									\
)

#define BIT_CLEAR(x, bit)	__extension__(				\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	b_	= (bit);				\
									\
	alx_Static_assert_unsigned(*x_);				\
	alx_assert_bit_fits_type(b_, *x_);				\
									\
	*x_	&= ~(1ull << b_);					\
	*x_;								\
}									\
)

#define BIT_FLIP(x, bit)	__extension__(				\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	b_	= (bit);				\
									\
	alx_Static_assert_unsigned(*x_);				\
	alx_assert_bit_fits_type(b_, *x_);				\
									\
	*x_	^= 1ull << b_;						\
	*x_;								\
}									\
)

#define BIT_READ(x, bit)	__extension__(				\
{									\
	__auto_type	x_	= (x);					\
	__auto_type	b_	= (bit);				\
	bool		r_;						\
									\
	alx_Static_assert_unsigned(x_);					\
	alx_assert_bit_fits_type(b_, x_);				\
									\
	r_	= x_ & (1ull << b_);					\
	r_;								\
}									\
)


