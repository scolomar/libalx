#define	ALX_AVG(a, b)	__extension__(					\
{									\
	((a) + (b)) / 2;						\
}									\
)

#define	ALX_AVGS(a, b)	__extension__(					\
{									\
	auto	a_	= (a);						\
	auto	b_	= (b);						\
									\
	(a_ / 2) + (b_ / 2) + (((a_ % 2) + (b_ % 2)) / 2);		\
}									\
)


