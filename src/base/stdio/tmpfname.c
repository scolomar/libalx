int	alx_tmpfname	(ptrdiff_t size, char fname[static restrict size])
{
	pid_t	ppid, pid;
	int	fd;

	ppid	= getppid();
	pid	= getpid();
	if (alx_snprintfs(fname, NULL, size,
			"/tmp/%s.ppid%"PRIpid".pid%"PRIpid".tmpfXXXXXXX",
				program_invocation_short_name, ppid, pid))
		return	-1;
	fd	= mkstemp(fname);
	if (fd < 0)
		return	-1;
	if (close(fd))
		return	-1;

	return	0;
}


