int	alx_fgets_nonl	(char buf[restrict /*bufsiz*/], int bufsiz,
			 FILE *restrict stream, ptrdiff_t *restrict len)
{
	ptrdiff_t	l;
	int		status;

	status	= 0;
	if (!fgets(buf, bufsiz, stream))
		goto err;

	l	= strnlen(buf, bufsiz);
	if (!l) {
		status	= ECANCELED;
		goto out;
	}
	if (buf[l-1] != '\n') {
		status	= ENOBUFS;
		goto out;
	}
	buf[--l] = '\0';

out:
	if (len)
		*len	= l;

	return	status;
err:
	if (len)
		*len	= 0;
	return	-errno;
}


