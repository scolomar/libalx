#include "libalx/base/signal/sigterm.h"

#include <signal.h>
#include <stdbool.h>
#include <stddef.h>

#include "libalx/base/compiler/unused.h"


/******************************************************************************
 ******* define ***************************************************************
 ******************************************************************************/


/******************************************************************************
 ******* enum / struct / union ************************************************
 ******************************************************************************/


/******************************************************************************
 ******* static variables *****************************************************
 ******************************************************************************/
sig_atomic_t	alx_sigterm;


/******************************************************************************
 ******* static prototypes ****************************************************
 ******************************************************************************/
static
void	sigterm_handler		(int sig);


/******************************************************************************
 ******* global functions *****************************************************
 ******************************************************************************/
int	alx_sigterm_init	(void)
{
	struct sigaction	sa = {0};

	alx_sigterm	= false;
	asm volatile ("" : : : "memory");

	sigemptyset(&sa.sa_mask);
	sa.sa_handler	= &sigterm_handler;
	return	sigaction(SIGTERM, &sa, NULL);
}


/******************************************************************************
 ******* alias ****************************************************************
 ******************************************************************************/
ALX_ALIAS_WEAK_DEF(sigterm,	alx_sigterm);
ALX_ALIAS_WEAK_DEF(sigterm_init,	alx_sigterm_init);


/******************************************************************************
 ******* static function definitions ******************************************
 ******************************************************************************/
static
void	sigterm_handler		(int sig)
{

	ALX_UNUSED(sig);

	alx_sigterm	= true;
	asm volatile ("" : : : "memory");
}


