int	alx_strscatfs	(char str[restrict /*nmemb*/],
			 ptrdiff_t *restrict written, ptrdiff_t nmemb,
			 const char *restrict format, ...)
{
	va_list	ap;
	int	status;

	va_start(ap, format);
	status	= alx_vstrscatfs(str, written, nmemb, format, ap);
	va_end(ap);

	return	status;
}

int	alx_vstrscatfs	(char str[restrict /*nmemb*/],
			 ptrdiff_t *restrict written, ptrdiff_t nmemb,
			 const char *restrict format, va_list ap)
{
	ptrdiff_t	len;

	if (nmemb <= 0)
		goto ovf;

	len	= alx_vstrscatf(nmemb, str, format, ap);

	if (len < 0)
		goto err;
	if (len >= nmemb)
		goto trunc;
	if (written)
		*written = len;

	return	0;
trunc:
	if (written)
		*written = nmemb - 1;
	errno	= ENOBUFS;
	return	1;
ovf:
	errno	= EOVERFLOW;
err:	if (written)
		*written = 0;
	return	-1;
}


