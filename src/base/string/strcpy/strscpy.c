ptrdiff_t alx_strscpy		(char dest[restrict /*size*/],
				 const char src[restrict /*size*/],
				 ptrdiff_t size)
{
	ptrdiff_t	len;

	if (size <= 0)
		return	-E2BIG;

	len	= strnlen(src, size - 1);
	memcpy(dest, src, len);
	dest[len] = '\0';

	return	len;
}


