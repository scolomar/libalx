/******************************************************************************
 ******* global functions *****************************************************
 ******************************************************************************/
int64_t	alx_timespec_ms			(const struct timespec *tm)
{
	return	tm->tv_sec * 1000ll  +  tm->tv_nsec / 1000000ll;
}

int64_t	alx_timespec_us			(const struct timespec *tm)
{
	return	tm->tv_sec * 1000000ll  +  tm->tv_nsec / 1000ll;
}

int64_t	alx_timespec_ns			(const struct timespec *tm)
{
	return	tm->tv_sec * 1000000000ll  +  tm->tv_nsec;
}


/******************************************************************************
 ******* alias ****************************************************************
 ******************************************************************************/
ALX_ALIAS_WEAK_DEF(timespec_ms,	alx_timespec_ms);
ALX_ALIAS_WEAK_DEF(timespec_us,	alx_timespec_us);
ALX_ALIAS_WEAK_DEF(timespec_ns,	alx_timespec_ns);


