int	alx_dirname_s	(ptrdiff_t size,
			 char dest[static restrict size],
			 const char path[restrict size])
{
	char	tmp[size];

	if (alx_strlcpys(tmp, path, ARRAY_SIZE(tmp), NULL))
		return	-1;
	if (alx_strlcpys(dest, dirname(tmp), size, NULL))
		return	-1;

	return	0;
}


