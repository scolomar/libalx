#pragma GCC diagnostic push	/* Overflow is explicitly handled */
#pragma GCC diagnostic ignored	"-Wsign-conversion"
void	*alx_mallocs__	(ssize_t size, int *error)
{
	void	*p;

	*error	= 0;
	if (!size)
		goto zero;
	if (size < 0 || size >= SSIZE_MAX)
		goto ovf;

	p	= malloc(size);
	if (!p)
		goto err;

	return	p;
ovf:
	errno	= ENOMEM;
err:
	*error	= -ENOMEM;
	return	NULL;
zero:
	*error	= ENOMEM;
	return	NULL;
}
#pragma GCC diagnostic pop


