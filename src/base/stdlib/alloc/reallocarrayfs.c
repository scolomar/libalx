void	*alx_reallocarrayfs__	(void *restrict ptr, ptrdiff_t nmemb,
				 size_t size, int *restrict error)
{

	*error	= 0;
	if (!nmemb || !size)
		goto zero;
	if (nmemb < 0)
		goto ovf;

	ptr	= alx_reallocarrayf(ptr, nmemb, size);
	if (!ptr)
		goto err;

	return	ptr;
ovf:
	errno	= ENOMEM;
	free(ptr);
err:
	*error	= -ENOMEM;
	return	NULL;
zero:
	free(ptr);
	*error	= ENOMEM;
	return	NULL;
}


