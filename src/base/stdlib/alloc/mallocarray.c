void	*alx_mallocarray	(ptrdiff_t nmemb, size_t size)
{

	if (!nmemb || !size)
		goto zero;
	if (nmemb < 0)
		goto ovf;
	if ((size_t)nmemb  >  (SIZE_MAX / size))
		goto ovf;

	return	malloc((size_t)nmemb * size);
ovf:
	errno	= ENOMEM;
zero:
	return	NULL;
}


