void	alx::CV::normalize	(class cv::Mat *img)
{
	cv::normalize(*img, *img, 0, UINT8_MAX, cv::NORM_MINMAX);
}

void	alx_cv_normalize	(void *img)
{
	return	alx::CV::normalize((class cv::Mat *)img);
}


