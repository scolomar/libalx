int	alx::CV::thinning	(class cv::Mat *img, int method)
{

	if (img->channels() != 1)
		return	1;

	cv::ximgproc::thinning(*img, *img, method);
	return	0;
}

int	alx_cv_thinning		(void *img, int method)
{
	return	alx::CV::thinning((class cv::Mat *)img, method);
}


